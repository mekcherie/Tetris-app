export const random = (min, max) => {
    return Math.floor(Math.random() * (max - min + 1)) + min
  }
  // Returns the default grid
export const gridDefault = () => {
    const rows = 18
    const cols = 10
    const array = []
    for (let row = 0; row < rows; row++) {
        array.push([])
        for (let col = 0; col < cols; col++) {
          array[row].push(0)
        }
    }
    return array
  }
